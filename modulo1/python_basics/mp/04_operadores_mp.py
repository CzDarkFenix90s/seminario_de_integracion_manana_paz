a = 150
b = 45
print("Suma")
print(a, "+", b, "=", a + b)
print("Resta")
print(a, "-", b, "=", a - b)
print("Multiplicacion")
print(a, "*", b, "=", a * b)
print("Division")
print(a, "/", b, "=", a / b)
print("Potencia")
print(a, "**", 2, "=", a ** 2)
print("Division Entera")
print(a, "//", b, "=", a // b)
print("Modulo")
print(a, "%", b, "=", a % b)

