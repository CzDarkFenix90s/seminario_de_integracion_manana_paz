# Operadores LOGICOS
x = True
y = False
print("AND:", x and y)
print("OR:", x or y)
print("NOT:", not x)

# Operadores de comparacion
unidad_a = 50
unidad_b = 80
print(f"{unidad_a} == {unidad_b}:", unidad_a == unidad_b)
print(f"{unidad_a} != {unidad_b}:", unidad_a != unidad_b)
print(f"{unidad_a} > {unidad_b}:", unidad_a > unidad_b)
print(f"{unidad_a} < {unidad_b}:", unidad_a < unidad_b)
print(f"{unidad_a} >= 50:", unidad_a >= 50)
print(f"{unidad_b} <= 100:", unidad_b <= 100)